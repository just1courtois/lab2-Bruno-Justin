public class DownloadFile {
    public static void downloadFile(S3Client s3, String bucketName, String key) {
        try {
            GetObjectRequest getObjectRequest = GetObjectRequest.builder()
                    .bucket(bucketName)
                    .key(key)
                    .build();

            s3.getObject(getObjectRequest);

        }
        catch(S3Exception e) {
            System.err.println(e.awsErrorDetails().errorMessage());
            System.exit(1);
        }
    }

    public static void main (String[]... args) {
        Region region = Region.US_WEST_1;
        S3Client s3 = S3Client.builder().region(region).build();
        String key = null;
        String bucket = null;
        Scanner input = new Scanner(System.in);  // Create a Scanner object
        System.out.println("Enter a valid file name");
        key = input.nextLine();  // Read user input
        System.out.println("Enter a valid bucket name");
        bucket = input.nextLine();  // Read user input
        input.close();
        downloadFile(s3, bucket, key);
        Selection.main();
    }

}
